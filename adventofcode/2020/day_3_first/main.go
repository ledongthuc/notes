package main

import "fmt"

func main() {
	var currentX, currentY int
	var treeCounter int
	for {
		if currentY >= len(geology)-1 { // stop at last row
			break
		}
		currentX, currentY = getNextPosition(currentX, currentY)
		if isHasTree(currentX, currentY) {
			treeCounter++
		}
	}
	fmt.Println("Last position: ", currentX, currentY, ", number of tree: ", treeCounter)
}

func isHasTree(currentX, currentY int) bool {
	if currentY >= len(geology) {
		return false
	}
	row := geology[currentY]
	currentX = currentX % len(row)
	if row[currentX] == '#' {
		return true
	}
	return false
}

func getNextPosition(currentX, currentY int) (nextX, nextY int) {
	return currentX + 3, currentY + 1
}

